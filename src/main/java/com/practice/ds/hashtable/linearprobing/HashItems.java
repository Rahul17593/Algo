package com.practice.ds.hashtable.linearprobing;

public class HashItems {
	private int key;
	private int val;

	public HashItems(int key, int val) {
		this.key = key;
		this.val = val;
	}

	public int getKey() {
		return key;
	}

	public int getVal() {
		return val;
	}

	public void setKey(int key) {
		this.key = key;
	}

	public void setVal(int val) {
		this.val = val;
	}

}
